class Calculater:
    def __init__(self,num):
        self.number=num
    def getSquare(A):
        print(f"{A.number} Square is : {A.number**2}")
    def getSquareRoot(A):
        print(f"{A.number} SquareRoot is : {int(A.number**0.5)}")
    def getQube(A):
        print(f"{A.number} Qube is : {A.number**3}")

a=int(input("Enter the num : "))         
